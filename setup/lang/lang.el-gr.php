[h4]Καταχωρήσεις (δημοσιεύσεις) και Στατικές σελίδες[/h4]

Αυτή είναι μία καταχώρηση, ενώ το [url=static.php?page=about]"Σχετικά"[/url] είναι μία [b]στατική σελίδα[/b]. Η στατική σελίδα είναι ουσιαστικά μία καταχώρηση (τυπική δημοσίευση) η οποία όμως δεν μπορεί να σχολιαστεί, ούτε και εμφανίζεται μαζί με τις υπόλοιπες δημοσιεύσεις στο ιστολόγιο.

Οι στατικές σελίδες είναι χρήσιμες για δημιουργία σελίδων γενικών πληροφοριών. Με την παρούσα έκδοση του FlatPress μπορείς να εμφανίσεις μία από αυτές τι σελίδες ως [b]Αρχική Σελίδα[/b] για τους επισκέπτες σου. Αυτό σημαίνει ότι πλέον μπορείς με το Flatpress να δημιουργήσεις κι ένα μη-ιστολογικό (non-blog) ιστότοπο. Η επιλογή να κάνεις μια στατική σελίδα την αρχική σου, είναι στις [b]Επιλογές[/b] της [url=admin.php]Περιοχής Διαχείρισης[/url]   


[h4]Πρόσθετα[/h4]

Το FlatPress είναι άκρως παραμετροποιήσιμο και υποστηρίζει [url=http://wiki.flatpress.org/doc:plugins]πρόσθετα[/url] που επεκτείνουν τη δύναμη του. Το ίδιο το BBCode είναι ένα πρόσθετο!

Έχουμε προ-δημιουργήσει λίγο περιεχόμενο ακόμη, για να σου δείξουμε μερικές ακόμη εξαιρετικές λειτουργίες του Flatpress! Θα βρεις δύο [b]στατικές σελίδες[/b] έτοιμες για να προσθέσεις το περιεχόμενο σου, ([url=static.php?page=about]Σχετικά[/url]) και συνδέσμους στο ([url=static.php?page=menu]Μενού[/url]): παρατήρησε πως οι σύνδεσμοι σου θα εμφανίζονται και στην δεξιά περιοχή μικροεφαρμογών επίσης, αυτή είναι η μαγεία της [b]μικροεφαρμογής blockparser[/b]: δες στο [url=http://wiki.flatpress.org/doc:faq]FAQ[/url] σχετικά με αυτή κι ακόμα περισσότερα. 


