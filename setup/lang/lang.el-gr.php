[h4]Διαμόρφωση[/h4]

Ο προεπιλεγμένος τρόπος διαμόρφωσης του περιεχομένου είναι το [url=http://wiki.flatpress.org/doc:plugins:bbcode]BBcode[/url] (bulletin board code). Το BBCode παρέχει έναν εύκολο τρόπο να μορφοποιήσεις τις δημοσιεύσεις σου. Οι πιο συνηθισμένοι κώδικες επιτρέπονται. Όπως [b] για [b]έντονα[/b], [i] για [i]πλάγια[/i], κ.ο.κ.

